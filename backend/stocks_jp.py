# (ticker(.T付き), name) のリスト。空名・重複は除外。
STOCKS: list[dict[str, str]] = []
_seen: set[str] = set()
for code, nm in _RAW:
    if not nm or not code.isdigit():
        continue
    t = f"{code}.T"
    if t in _seen:
        continue
    _seen.add(t)
    STOCKS.append({"ticker": t, "name": nm})

_BY_TICKER = {s["ticker"]: s["name"] for s in STOCKS}


def lookup_name(ticker: str) -> str | None:
    """内蔵マスタから銘柄名を返す（無ければ None）。"""
    return _BY_TICKER.get(ticker)


def search(query: str, limit: int = 20) -> list[dict[str, str]]:
    """名前またはコードの部分一致で検索（最大 limit 件）。"""
    q = (query or "").strip().lower()
    if not q:
        return []
    # コード検索用に .T や数字を正規化
    q_code = q.replace(".t", "")
    hits = []
    for s in STOCKS:
        name_l = s["name"].lower()
        code = s["ticker"].lower().replace(".t", "")
        if q in name_l or q_code and q_code in code:
            hits.append(s)
        if len(hits) >= limit:
            break
    return hits
